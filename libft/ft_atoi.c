int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	tot;

	i = 0;
	sign = 1;
	tot = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		sign *= -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9' )
	{
		tot = tot * 10 + str[i] - '0';
		i++;
	}
	return (tot * sign);
}
